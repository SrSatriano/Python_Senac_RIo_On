# Exercício 03 
# Cálculo da área de um retângulo: solicite ao usuário que insira a largura e a
# altura do retângulo, calcule e exiba a área deste retângulo.

largura = float(input("Qual é a largura do retângulo? "))
altura = float(input("Qual é a altura do retângulo? "))

area_do_retangulo = largura * altura

print(f"A area do retângulo é: {area_do_retangulo}")
